#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn known_glyphs_resolve() {
        for c in ['A', 'Z', 'a', 'z', '0', '9', ' ', ':', '%'] {
            assert!(glyph(c).is_some(), "missing glyph for {c:?}");
        }
    }

    #[test]
    fn unsupported_chars_return_none() {
        for c in ['√', '中', '€', '\t'] {
            assert!(glyph(c).is_none(), "unexpected glyph for {c:?}");
        }
    }
}
